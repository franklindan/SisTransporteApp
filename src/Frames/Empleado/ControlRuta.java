    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        try {
            conn=App.ConnectionBd.Enlace(conn);
            st=App.ConnectionBd.sta(st);
            
            String hora,fecha,descp,placa,sSQL;
            int empledni,paraid,transid;
            
            hora=txtHora.getText();
            fecha=txtDatechoser.getText();;
            descp=txtaDescp.getText();
            placa=txtPlaca.getText();
            paraid=Integer.parseInt(txtParadero.getText());
            empledni=Log.dni;
            transid=Log.transporte;

            if (placa.equals("")){
                JOptionPane.showMessageDialog(null, "Campos sin llenar");
                txtPlaca.grabFocus();
            } else {
                sSQL="insert into Control (horaControl,fechControl,descpControl,Empleado_dniEmpl,Vehiculo_placVehi,Paradero_idPara,"
                        + "Paradero_Transporte_idTran)values ('"+hora+"','"+fecha+"','"+descp+"','"+empledni+"','"+placa+"','"+paraid
                        +"','"+transid+"');";
                try{
                    Statement st=(Statement) conn.createStatement();
                    st.executeUpdate(sSQL);
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                } catch (SQLException ex){
                    JOptionPane.showMessageDialog(null, "Introduzca los datos correctamente");
                }
                
                limpia();
                //buscar("");
                conn.close();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al insertar el dato, verifique porfavor.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Introduzca los datos correctamente");
        }
    }//GEN-LAST:event_btnAgregarMouseClicked

  

